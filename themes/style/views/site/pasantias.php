                   <p>
                   Las prácticas profesionales son necesarias para darte una primera visión de cómo es trabajar en una empresa y de las 		responsabilidades que deberás afrontar. Además de <br />ser un requisito para graduarte, con las pasantías tendrás una nueva perspectiva sobre lo que realmente quieres para tu carrera profesional.<br /><br />

<strong>Conoce las ventajas de hacer una pasantía.</strong><br />

<ul type="circle"> 

<li>Aprenderás a ser responsable y afrontarás problemas reales que te convertirán en una persona competente.</li><br />

<li>Tendrás nuevas experiencias que agregar a tu currículum.</li><br />

<li>Conocerás el comportamiento de la empresa y, por ende, sabrás cómo desenvolverte en el mundo empresarial.</li><br />

<li>Con la experiencia obtenida tendrás mayor posibilidad de encontrar trabajo rápidamente. Además, dependiendo de tu disposición y comportamiento en la organización podrás ser seleccionado para ocupar un puesto formalmente.</li><br />

<li>Obtendrás nuevos contactos que ayudarán a aumentar la red de profesionales en tu campo.</li><br />

<li>Desarrollarás proyectos a los que las empresas no pueden dedicarle el 100% de su tiempo. Por lo que esta situación puede ser una gran oportunidad para demostrar tus habilidades.</li><br />

</ul>
Es importante que tomes en cuenta que las pasantías se tratan de un convenio académico entre la universidad y la empresa. Por lo tanto, no existe un contrato que establezca beneficios laborales con el estudiante. También hay organizaciones que dan un pago como ayuda a los pasantes más no es una obligación que se deba cumplir.<br /><br />

Esta información fue tomada de ¿Por qué tienes que hacer prácticas en una empresa? de Iberestudios.com<br /><br /><br />

<strong>El proceso y los requisitos fundamentales para hacer las pasantías se describen a continuación:</strong><br />


<ol>

<li>Tener Aprobadas Todas las Asignaturas del Área Académica.</li><br />
<li>Tener Aprobado el Servicio Comunitario.</li><br />
<li>Preinscribir la Pasantía en Control de Estudio en el Lapso Académico Actual.</li><br />
<li>Seleccionar la empresa de tu Preferencia.</li><br />
<li>Ubicar el Tutor externo de la empresa seleccionada.</li><br />
<li>Desarrollar anteproyecto de Pasantías en base a los formatos oficiales UNET.</li><br />
<li>Presentar anteproyecto a la Comisión del TAP del Departamento de tu carrera para su aprobación.</li><br />
<li>Una vez Aprobado Comienza el Tiempo de 16 Semanas Estipulado para tu Pasantía.</li><br />
<li>La comisión Asignará tu tutor Académico que junto al tutor externo son los encargados de guiarte y evaluarte durante la pasantía.</li><br />
<li>Al Final de las 16 semanas Debes preparar el informe final y presentarlo a la comisión del TAP para la asignación de jurados.</li><br />
<li>Una vez revisado y aprobado por los jurados realizas la defensa y finaliza la pasantía.</li><br />
</ol>

