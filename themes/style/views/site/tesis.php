                   <p>
                  La tesis es la propuesta de un tema relacionado con la carrera de Ingeniería en Informática, para investigar y fundamentar con los conocimientos adquiridos durante el</br> estudio de la misma. Un jurado seleccionado por la comisión del TAP, se encargará de leer y evaluar el documento escrito de la investigación para verificar el cumplimiento</br> de los objetivos propuestos (Propuesta de Trabajo Especial de Grado). También consta de una exposición sobre la investigación, donde el jurado y tutor harán preguntas</br> para apoyar o rebatir lo expuesto en el trabajo escrito y deberás defender el punto de vista con argumentos sólidos que sustenten la tesis.<br /><br />

<strong>Ventajas de realizar tesis:</strong><br />

<ul type="circle"> 

<li>Mayor libertad para eligir un tema respecto a la pasantía, puedes seleccionarlo dependiendo de tus intereses o experiencia en un área, siempre y cuando se haga a partir de textos o estudios previos.</li><br />

<li>Aprender a hacer una investigación utilizando el método científico.</li><br />

<li>Aprendes a ordenar el pensamiento y a desarrollar una investigación siguiendo una metodología de desarrollo de software (aplicación de la ingeniería).</li><br />

<li>Oportunidad de mostrar lo aprendido en la carrera y experiencia cultural.</li><br />

<li>Obtendrás nuevos contactos que ayudarán a aumentar la red de profesionales en tu campo.</li><br />

<li>Se aprende a utilizar correctamente el uso de bibliografías y hemerografías pertinentes al trabajo, ademas del criterio selectivo a través de selección de información entre muchas lecturas.</li><br />

<li>Se aprende la importancia de delimitar y cumplir un problema de investigación.</li><br />

<li>Experiencia en el desarrollo de una tesis, facilitando el proceso de crear tesis al optar títulos de postgrado como maestrías y doctorados.</li><br />
<li>Permite crear un perfil laboral, de investigación y mostrar tu forma de trabajar.</li><br />

</ul>


<strong>Desventajas de realizar tesis:</strong><br />


<ol>

<li>Requiere de un investigador responsable, capaz de realizar todo el proceso de la investigación por su cuenta, sin la necesidad de tener un superior pendiente en el avance.</li><br />
<li>No agrega experiencia laboral respecto a como se desarrollan las actividades en una organización, metodologías usadas en una compañía, relaciones laborales, entre otros.</li><br />
<li>Dependiendo del tema elegido, puede llevar mayor tiempo respecto a la tesis.</li><br />

</ol>

