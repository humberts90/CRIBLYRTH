                   <p align="justify" >
                   
                   		El Trabajo de Aplicación Profesional (TAP) es una actividad académica que contribuye a la formación integral del alumno y conduce al desarrollo de las competencias generales para asumir, desde un pensamiento complejo, las situaciones para enfrentar en su desempeño profesional y personal, así como la búsqueda permanente del desarrollo humano sostenible y la identificación de su contexto social.
                   
                      <br />
                      <br />
                      
						<strong>PROYECTO ESPECIAL DE GRADO</strong><br /> <br />
 


					<!--<div class="textoplano">-->
   						El Proyecto Especial de Grado es una de las modalidades previstas en el Trabajo de Aplicación Profesional y se entiende como una actividad académica orientada a la aplicación, ampliación o profundización de los conocimientos adquiridos a lo largo del plan de estudios de la carrera. El Proyecto Especial de Grado comprende el estudio sistemático, orientado al planteamiento y contribución en la solución de un problema o, un esfuerzo de creación que demuestre el dominio en un área específica. El Proyecto Especial de Grado puede ser desarrollado o efectuado en un organismo, empresa o unidad académica.
                <br />
                <br />
						
						<strong>PASANTÍAS PROFESIONALES</strong><br /> <br />
							La Pasantía Profesional, es una de las modalidades previstas en el Trabajo de Aplicación Profesional y se entiende como la aplicación de los conocimientos y competencias adquiridos por el alumno durante sus estudios de pregrado, para abordar y ofrecer soluciones a necesidades, problemas o situaciones existentes en el organismo o empresa receptora.
